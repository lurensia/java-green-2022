package ex05;

public class VarEx01 { // Varx01 static
    public static void main(String[] args) { // main stack
        byte num; // 변수의 선언(매모리에 공간 확보)
        num = 1;// 확보하고 대입
        byte num2 = 100; // 변수의 초기화(확보 - 대입) 확보하면서 대입
        System.out.println(num); // num 출력
        System.out.println(num2); // num 출력
    }
}
