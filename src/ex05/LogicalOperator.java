package ex05;

public class LogicalOperator {
    public static void main(String[] args) {
        // 스택
        int x = 3;
        int y = 4;
        // 논리연산자
        // %% 그리고 (AND)
        // || 이거나 (OR)
        System.out.println((x == 3) && (y == 7));
        System.out.println((x == 3) || (y == 4));
    }
}
