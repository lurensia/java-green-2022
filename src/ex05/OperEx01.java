package ex05;

public class OperEx01 {
    public static void main(String[] args) {
        System.out.println(1 + 1); // 덧셈 연산 =2
        System.out.println(1 - 1); // 뺄셈 연산 = 0
        System.out.println(2 * 2); // 곱셈 연산 = 4
        System.out.println(2 / 2); // 나누기 연산 = 1
        System.out.println(9 % 7); // 나머지 연산 =2(9를 7로 나누고 남은것)

        // 더하기(산술더하기 or 결합)

        System.out.println("1" + 1); // = 11(문자와 숫자가 + 되면 그냥 이어진다.)
    }
}